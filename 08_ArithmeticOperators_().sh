'Using (( ))
    The (( )) syntax is preferred for arithmetic operations as it is more straightforward and allows for more complex expressions.'

a=10
b=5

#Addition
result=$((a+b))
echo "Addition: $result"

#Subtraction
result=$((a-b))
echo "Subtraction:$result"


#Multiplication
result=$((a*b))
echo "Multiplication: $result"

#Division
result=$((a/b))
echo "Division: $result"

#Modulus
result=$((a%b))
echo "Modulus: $result"
